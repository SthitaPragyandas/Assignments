class Literals{
	public static void main(String[] args){
		char c1='a';
		System.out.println("c1 = " + c1);
		int i1 = 10;
		System.out.println("i1 = " + i1);
		int i2 =0456;
		System.out.println("i2 = " + i2);
		int i3 =0b10010;
		System.out.println("i3 = " + i3);
		int i4 =0xABCD;
		System.out.println("i4 = " + i4);
	}
}